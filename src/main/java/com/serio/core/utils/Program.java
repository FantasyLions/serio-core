package com.serio.core.utils;

public class Program {

	private String tvProgramTime;
	private String tvProgramName;

	public String getTvProgramTime() {
		return tvProgramTime;
	}

	public void setTvProgramTime(String tvProgramTime) {
		this.tvProgramTime = tvProgramTime;
	}

	public String getTvProgramName() {
		return tvProgramName;
	}

	public void setTvProgramName(String tvProgramName) {
		this.tvProgramName = tvProgramName;
	}

}
